      <h2>Ballad of Birmingham | Spring 2015</h2>

      <p>The rhyme scheme used was fairly simple. The author chose to alternate rhyming lines, leaving every other line to rhyme. The author also uses Anaphora in lines 18-20 when he starts each line with “And.” This helps draw attention the ideas that are spanning multiple lines. The language is the most powerful part of the poem. The entire poem was about a very emotional event. The bombing of a church and the loss of a child. The author does a very good job leaving the ending open but closed. They also manage to play with the audience’s values when the child dies, leaving the parent to mourn their loss.</p>

